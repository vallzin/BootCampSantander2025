package br.com.dio.persistence.entity;

public class BoardEntity {

    private Long id;
    private String name;

    public BoardEntity() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
